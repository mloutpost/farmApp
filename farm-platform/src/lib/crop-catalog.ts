export function getCropById(id: string): CropEntry | undefined {
  return CROP_CATALOG.find((c) => c.id === id);
}

export function searchCrops(query: string): CropEntry[] {
  if (!query.trim()) return CROP_CATALOG;
  const q = query.toLowerCase();
  return CROP_CATALOG.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.category.replace("-", " ").includes(q) ||
      (c.botanical && c.botanical.toLowerCase().includes(q))
  );
}

export function getCropsByCategory(category: CropCategory): CropEntry[] {
  return CROP_CATALOG.filter((c) => c.category === category);
}
